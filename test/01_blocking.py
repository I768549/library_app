"""
Урок 01 — НАИВНАЯ (плохая) версия.

Цель этого файла — почувствовать проблему руками.
Запусти: python test/01_blocking.py
И попробуй:
    1. Нажать кнопку "Загрузить".
    2. Пока крутятся 3 секунды — подвигай окно мышью.
       → не двигается, "залипло".
    3. Понажимай кнопку ещё несколько раз во время "загрузки".
       → нажатия копятся, потом срабатывают сразу.
    4. Попробуй закрыть окно крестиком во время "загрузки".
       → не закрывается, ОС может показать "приложение не отвечает".

Почему так? Tkinter — однопоточный. Весь UI рисуется и реагирует на события
в одном цикле — mainloop(). Когда ты в обработчике кнопки делаешь time.sleep(3),
ты на 3 секунды БЛОКИРУЕШЬ этот цикл. Mainloop не успевает:
    - перерисовать окно
    - обработать клики
    - обновить Label, даже если ты вызвал label.config() ДО sleep
Всё это становится в очередь и разгребается только когда обработчик вернёт управление.

В следующем уроке вынесем "тяжёлую работу" в отдельный поток.
"""
import tkinter as tk
import time


def fake_db_query():
    """Имитация долгого запроса к БД. В реальности тут был бы SELECT."""
    time.sleep(3)
    return ["Книга 1", "Книга 2", "Книга 3"]


def on_load_click():
    # Этот код выполняется в ГЛАВНОМ потоке Tk (как и весь mainloop).
    # Пока мы тут — Tk не может рисовать и обрабатывать события.
    status_label.config(text="Загружаю...")
    # ↑ эта строка отработает, НО на экране ты её не увидишь:
    #   перерисовать окно Tk сможет только после возврата из обработчика,
    #   а до этого — sleep на 3 секунды.

    books = fake_db_query()  # ← ВОТ ОНО, БЛОКИРОВКА на 3 секунды

    # Сюда дойдём только через 3 секунды.
    status_label.config(text=f"Загружено: {len(books)} книг")
    listbox.delete(0, tk.END)
    for b in books:
        listbox.insert(tk.END, b)


# --- UI ---
root = tk.Tk()
root.title("01 — Блокирующая версия (плохо)")
root.geometry("400x300")

tk.Button(root, text="Загрузить", command=on_load_click).pack(pady=10)

status_label = tk.Label(root, text="Готов")
status_label.pack(pady=5)

listbox = tk.Listbox(root)
listbox.pack(fill="both", expand=True, padx=10, pady=10)

root.mainloop()
